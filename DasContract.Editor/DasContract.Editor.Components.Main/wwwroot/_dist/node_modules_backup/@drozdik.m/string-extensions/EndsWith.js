//--------------------------------------------------
//---------ENDS WITH--------------------------------
//--------------------------------------------------
String.prototype.EndsWith = function (compareString) {
    var currentString = this;
    //Input string is longer that current string
    if (compareString.length > currentString.length)
        return false;
    //Iterate and check if all chars are equal
    for (var i = currentString.length - 1, j = compareString.length - 1; i >= 0 && j >= 0; i--, j--) {
        //Found mistake
        if (currentString[i] !== compareString[j])
            return false;
    }
    //It's a match
    return true;
};
