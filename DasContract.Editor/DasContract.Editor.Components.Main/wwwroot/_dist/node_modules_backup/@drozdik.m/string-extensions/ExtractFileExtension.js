String.prototype.ExtractFileExtension = function () {
    var filePath = this;
    //String is empty
    if (filePath.length == 0)
        return "";
    //String ends with "."
    if (filePath.charAt(filePath.length - 1) == ".")
        return "";
    var split = filePath.split(".");
    //No dots
    if (split.length <= 1)
        return "";
    return split[split.length - 1];
};
